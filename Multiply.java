public class Multiply {
    public static int multiply(int num1, int num2){
        return num1 * num2;
    }

    public static void main(String[] args) {
        int result1 = multiply(2, 3);
        System.out.println("multiply(2, 3) = " + result1);

        int result2 = multiply(0, 188);
        System.out.println("multiply(0, 188) = " +result2);

        int result3 = multiply(85, 144);
        System.out.println("multiply(85, 144) = " +result3);
    }
}
